package application;

public class 배열 {

	public static void main(String[] args) {
		// 배열은 같은 타입의 여러 변수를 하나로 묶은 자료형 (참조 자료형)
		
		int[] numbers = {2, 4, 6, 8};
		
		System.out.println(numbers[0]);
		System.out.println(numbers[1]);
		System.out.println(numbers[2]);
		System.out.println(numbers[3]);

	}

}
